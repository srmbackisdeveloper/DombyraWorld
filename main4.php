		<h1>Конструкция</h1>
    
    	<p class="lead"  >
    Длина домбры составляет 80—130 см. Лады могут быть навязными, врезными, или, как у узбекской домры, отсутствовать совсем.<br>

Шанак  — корпус домбры, выполняет роль усилителя звука.<br>
Какпак — дека домбры. Воспринимая посредством вибрации звуки струн, усиливает их и придаёт определённую окраску звучанию инструмента — тембр.<br>
Пружина — это балка на деке с внутренней стороны. В казахской домбре пружины раньше не было. У домбры сейчас для улучшения звука аналогичная пружина крепится в верхней части панциря и возле подставки. Как правило, она изготовляется из ели с выдержкой в несколько десятилетий без признаков гнили.<br>
Порожки — разъединяют «клавиши» на домбре.<br>
Обечайки изготавливаются из клёна.<br>
Подставка (тиек) — очень ответственный функциональный элемент домбры. Передавая колебания струн на деку и создавая первый резонансный контур на пути распространения колебаний от струн к корпусу, подставка является подлинным ключом к звучанию домбры. От её качеств, формы, веса и настройки зависят сила, ровность и тембр звучания инструмента.<br>
Струна — источник звуковых колебаний домбры. На домбре традиционно использовались жильные струны, изготавливаемые из бараньих или козьих кишок. Но наиболее подходящей по звучанию оказалась обычная рыболовная леска. Как результат сегодня мы имеем единственный, широко распространённый вид домбры стандартной формы со струнами из лески, утративший неповторимый тембр звучания.
