			<h1>Строи</h1>
<p class="lead">
Звучание открытых струн домбры образует её квартовый строй. Также он может быть и квинтовым. Последовательность тонов для домбры, начиная с первой струны, самой высокой по тону: Соль, Ре (малой октавы).<br>

Интервалы между струнами: g{ч.4}d (буквенная нотация по Гельмгольцу, ч.4 — чистая кварта).<br>

Музыкальный диапазон домбры с 19 ладами на грифе составляет две полных октавы (часть малой октавы, первую и часть второй): от Ре малой октавы до Ре второй октавы.<br>

